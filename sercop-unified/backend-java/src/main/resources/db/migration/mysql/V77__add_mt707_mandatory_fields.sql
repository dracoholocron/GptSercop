-- =====================================================
-- Verify final configuration
-- =====================================================
SELECT field_code, field_name, is_required, component_type, draft_field_mapping
FROM swift_field_config_readmodel
WHERE message_type = 'MT707' AND language = 'en'
ORDER BY display_order;
